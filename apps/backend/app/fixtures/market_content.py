def list_signals() -> list[dict[str, Any]]:
    items = []
    for signal in _canonical_signals:
        signal_id = build_signal_id(signal)
        symbol_id = _symbol_id_by_symbol[signal["symbol"]]
        items.append(
            {
                "id": signal_id,
                "symbolId": symbol_id,
                "marketType": _market_type_by_symbol_id[symbol_id],
                "status": _signal_statuses[signal_id],
                "signal": signal,
            }
        )
    return _clone(items)


